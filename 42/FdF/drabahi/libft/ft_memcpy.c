#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	if (!s1 || !s2)
		return (s1);
	while (n-- > 0)
		((unsigned char *)s1)[n] = ((unsigned char *)s2)[n];
	return (s1);
}
