#include <stdlib.h>
#include "libft.h"

void		ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*lst;
	t_list	*trash;

	if (!alst)
		return ;
	lst = *alst;
	while (lst)
	{
		trash = lst;
		(*del)(lst->content, lst->content_size);
		lst = lst->next;
		free(trash);
	}
	*alst = NULL;
}
